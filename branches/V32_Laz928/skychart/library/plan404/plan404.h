
struct PlanetData
{
   double JD;	// julian date
   double L;	// heliocentric ecliptic longitude ( radians )
   double B;	// heliocentric ecliptic latitude
   double R;	// radius vector
   double X;	// heliocentric equatorial rectangular coordinates
   double Y;	// ( AU )
   double Z;
   int ipla;	// planet number	
};
